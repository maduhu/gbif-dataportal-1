package org.gbif.portal.model.occurrence;

import org.gbif.portal.model.BaseObject;

/**
 * Simple model class for institution code, collection code
 * and catalogue number.
 * 
 * @author dmartin
 */
public class IdentifierCode extends BaseObject {
	
	/** The code this model object wraps */
	private String code;

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
}